def build_generation_prompts(schema, data_samples, count_per_complexity):
    system_prompt = (
        "Ты — Data Scientist. Твоя задача — создать бенчмарк для тестирования систем GraphRAG. "
        "Тебе даны схема графовой базы Neo4j и ПРИМЕРЫ реальных данных из нее. "
        "Ты должен вернуть строго валидный JSON-массив объектов. Без markdown, без текста до/после. "
        "Генерируй только вопросы, на которые можно дать однозначный ответ по данным графа."
    )

    user_prompt = f"""
    Ты — Senior Neo4j Architect и эксперт по оценке систем GraphRAG. 
Твоя задача — создать "Золотой стандарт" (Ground Truth) датасета для тестирования того, насколько хорошо AI-системы извлекают информацию из графа знаний.

Ниже представлена точная схема текущей базы данных и примеры реальных данных из неё (Data Grounding). Твоя задача — сгенерировать набор пар "Вопрос на естественном языке" и "Точный Cypher-запрос, отвечающий на этот вопрос".

=== ВХОДНЫЕ ДАННЫЕ ===
1. СХЕМА ГРАФА (Labels, Relationships, Properties):
{schema}

2. ПРИМЕРЫ ДАННЫХ (Реальные значения атрибутов для привязки к реальности):
{data_samples}

=== ПРАВИЛА ГЕНЕРАЦИИ (КРИТИЧЕСКИ ВАЖНО) ===
1. СТРОГАЯ СХЕМА: Тебе ЗАПРЕЩЕНО использовать узлы (Labels), связи (Relationships) или свойства (Properties), которых нет в разделе "СХЕМА ГРАФА". Направление связей (->) должно строго соблюдаться.
2. ГАРАНТИЯ ДАННЫХ: Используй значения из раздела "ПРИМЕРЫ ДАННЫХ" для фильтрации в Cypher (например, в блоках WHERE или внутри `{{}}`). Твой Cypher-запрос не должен возвращать пустой результат.
3. ЕСТЕСТВЕННЫЙ ЯЗЫК: Вопросы должны звучать так, как их задал бы реальный аналитик или исследователь. Не пиши технически: "Найти все узлы X, связанные с узлом Y". Пиши по смыслу: "Какие компании сотрудничают с X?" или "Кто руководит отделом Y?".
4. ОГРАНИЧЕНИЕ ВЫВОДА: Возвращай в RETURN конкретные свойства (например, n.name, n.title), а не сами узлы (n).
5. КОНКРЕТИКА И ПРИВЯЗКА К СУЩНОСТЯМ (ENTITY ANCHORING): 
При поиске начального узла (фильтрация внутри `{{}}` или `WHERE`) КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО использовать только общие, категориальные или булевы свойства (например, sentiment: 'negative', sector: 'Tech', status: 'active'). 
Ты ОБЯЗАН привязывать запрос к конкретному уникальному объекту из "ПРИМЕРОВ ДАННЫХ", используя его идентифицирующие свойства (name, title, ticker, id). 
Вопрос должен быть сфокусированным. 
- ПЛОХО: "Кто CEO компании из негативной новости?" (MATCH {{"sentiment": 'negative'}})
- ХОРОШО: "Кто CEO компании, которая упоминается в статье 'Apple faces supply chain delays...'?" (MATCH {{title: 'Apple faces supply chain delays...'}})

=== КАТЕГОРИИ СЛОЖНОСТИ (Сгенерируй по {count_per_complexity} вопросов для каждой) ===
- "simple": Извлечение атрибутов одного конкретного узла или его прямых соседей (1 hop).
- "multi-hop": Вопросы, требующие прохождения цепочки из 2-4 связей между разными типами узлов. Это должны быть вопросы на поиск скрытых связей, неочевидных зависимостей или аналогий.
- "aggregation": Аналитические вопросы, использующие COUNT, MAX, MIN, AVG, ORDER BY или LIMIT (например, топ-5, количество, самый частый).

=== ФОРМАТ ВЫВОДА ===
Ты должен вернуть ТОЛЬКО валидный JSON-массив без дополнительных комментариев, маркдаун-блоков (```json) или вступлений. Строгая структура:

[
  {{
    "complexity": "simple | multi-hop | aggregation",
    "question": "Текст вопроса на русском языке",
    "cypher": "MATCH ... RETURN ..."
  }}
]
    """
    return system_prompt, user_prompt
